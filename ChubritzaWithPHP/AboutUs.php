        <p> 
            
          Along with the variety of dishes, in Bulgaria an ancient tradition is the preparation of home-made alcohol - the most common of which is home-made rakiya and home-made wine. Home-made raliya can be prepared from different types of fruits - grapes, plums, apricots, pears. After their fermentation, they are poured into pots, where they boil on an incendiary fire and the vapors that are converted into liquid drain into a vessel. The first drops of rakiya have a very high degree and they are called "first". In order to soften the flavor of the brandy or to obtain the color in the pots, there were branches of trees, sugar, and in some cases anise, which gives the specific taste and aroma of the drink.
        </p>
        <p>
            Home-made Bulgarian wine is prepared from grapes, with the Bulgarian lands possessing exceptional good wine varieties known all over the world. The Thracian region of Bulgaria is filled with very good vineyards, from which the preparation of wine turns into magic. Most of the varieties of grapes in this region are Pamid, Smoke (in the region of Chirpan) and Mavrud (mostly in the region of Asenovgrad). Wine production also begins with fermentation of grape juice. Once the grapes are crushed and poured into a barrel they are allowed to "boil" for a few weeks. In the earlier years, the process of grape grain milling was carried out by beautiful Bulgarian unmarried girls, who legged the fruit with the legs and so it was believed that the wine would be very good for taste and aroma. Sugar is then added and "prewashed", which means that the grapes are stirred in the barrel and the grape juice is sputtered, draining a certain quantity and returning to the barrel.
